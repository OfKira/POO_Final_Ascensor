//Clase que representa un piso del edificio.

public class Floor {
// Número que identifica el piso
    private int number;
//  Constructor que inicializa el piso con un número específico.
    public Floor(int number) {
        this.number = number;
    }
// Obtiene el numero de piso
    public int getNumber() {
        return number;
    }
}

